"""
Streamlit Web Interface for the HomeFinder multi-agent system.

This module provides a user-friendly dashboard for real estate analysis,
allowing users to input property URLs, financial parameters, and view
comprehensive evaluation reports generated by the LangGraph backend.
"""

import asyncio
from typing import Any, Dict, cast

import markdown
import pandas as pd
import plotly.express as px
import plotly.graph_objects as go
import streamlit as st

from src.agents.nodes import (
    commuter_node,
    data_extractor_node,
    evaluator_node,
    financial_node,
    negotiator_node,
    osint_node,
)
from src.core.config import (
    CLAUDE_HAIKU,
    CLAUDE_SONNET,
    GEMINI_FLASH,
    GEMINI_FLASH_LITE,
    GPT_4O,
    GPT_4O_MINI,
    KIMI_V1_8K,
    KIMI_V1_32K,
)
from src.core.graph import app as graph_app
from src.core.logging_config import setup_logging
