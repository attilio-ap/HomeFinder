# ─────────────────────────────────────────────────────────────────────────────
# 1. PAGE CONFIGURATION
# ─────────────────────────────────────────────────────────────────────────────
st.set_page_config(page_title="Home Finder", page_icon="🏢", layout="wide")

# ─────────────────────────────────────────────────────────────────────────────
